/**
 * 행정동 이름 → 법정동 이름 변환.
 * sigungu 매핑이 있으면 우선, 없으면 regex (\d+동$ → 동) fallback.
 */
export function adminToLegalDong(adminName: string, sigunguName?: string): string {
  if (!adminName) return adminName;
  if (sigunguName) {
    const map = SEOUL_LEGAL_DONG[sigunguName];
    if (map && map[adminName]) return map[adminName];
  }
  // fallback: regex strip (서초3동 → 서초동)
  return adminName.replace(/(.+?)\d+동$/, '$1동');
}

